#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		x;
	char	*s1;

	x = 0;
	s1 = (char *)s;
	while (s1[x] != '\0')
	{
		if (s1[x] == c)
			return (&s1[x]);
		x++;
	}
	if (c == '\0')
		return (&s1[x]);
	return (0);
}
