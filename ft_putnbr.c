#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > 2147483647 || n < -2147483648)
		return ;
	if (n > 9 || n < -9)
		ft_putnbr(n / 10);
	if (n < 0)
	{
		if (n >= -9)
			ft_putchar('-');
		ft_putchar('0' - (n % 10));
	}
	else
		ft_putchar((n % 10) + '0');
}
